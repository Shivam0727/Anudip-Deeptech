class OperatorExample
{
	public static void main(String args[])
	{
		int a=18;
		int b=7;
                System.out.print("The addition of two operands are : ");
		System.out.println(a+b);
		System.out.print("The Subtraction of two operands are : "); 
		System.out.println(a-b); 
		System.out.print("The Multiplication of two operands are : ");
		System.out.println(a*b); 
		System.out.print("The Division of two operands are : ");
		System.out.println(a/b);
	}
}