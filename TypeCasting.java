
class TypeCasting {
    public static void main(String[] args)
    {
            
        char ch1 = 'B';  
	char ch2 = 'C';
	char ch3 = 'D';         
        byte x = (byte) ch1 ;  
	byte y = (byte) ch2 ;
	byte z = (byte) ch3 ;       
        System.out.println(x);
	System.out.println(y);
	System.out.println(z);
    }
}